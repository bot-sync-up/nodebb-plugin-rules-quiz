/**
 * If the questions store has fewer than `targetCount` entries, insert the
 * missing ones from `SEED_QUESTIONS`. Admins can reseed explicitly even
 * when there are already some questions.
 *
 * @param {object} db the `lib/db` module (so we stay decoupled)
 * @param {{ targetCount?: number, force?: boolean }} [opts]
 * @returns {Promise<{seeded:number, total:number}>}
 */
async function seedIfEmpty(db, opts) {
  const options = opts || {};
  const target = Number.isFinite(options.targetCount) ? options.targetCount : SEED_QUESTIONS.length;
  const existing = await db.listQuestions({});
  const have = Array.isArray(existing) ? existing.length : 0;
  if (!options.force && have >= target) return { seeded: 0, total: have };
  const startFrom = options.force ? 0 : have;
  let seeded = 0;
  for (let i = startFrom; i < SEED_QUESTIONS.length; i++) {
    try {
      await db.createQuestion(SEED_QUESTIONS[i]);
      seeded += 1;
    } catch (_) { /* skip bad insert */ }
  }
  return { seeded, total: have + seeded };
}

module.exports = { seedIfEmpty, SEED_QUESTIONS };
