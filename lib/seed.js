'use strict';

/**
 * Starter question bank for nodebb-plugin-rules-quiz.
 *
 * On activation (or when the admin clicks "Re-seed from pool") we populate
 * the question store with a set of ~60 diverse Hebrew forum-etiquette
 * questions.
 *
 * v0.6.0: Every question carries at least one GATE tag in addition to its
 * topical tag(s). Gates are:
 *   - 'onboarding' — long first-time quiz covering fundamentals
 *   - 'post'       — short mini-quiz before a reply (reply-specific etiquette)
 *   - 'topic'      — pre-new-thread quiz (titles, categories, spam, duplicates)
 * A question may belong to multiple gates.
 *
 * When settings.pool.mode === 'single_tagged' the server filters questions
 * per gate using `questionsForGate(gate)`.
 */

function mk(title, body, options, explanation, tags, sort) {
  return {
    type: 'single',
    title: title,
    bodyMarkdown: body || '',
    options: options,
    explanationMarkdown: explanation || '',
    weight: 1,
    tags: tags || [],
    sort: sort,
  };
}
function opts(correctIdx, texts) {
  return texts.map(function (t, i) {
    return { id: String.fromCharCode(97 + i), text: t, correct: i === correctIdx };
  });
}

